# Вводим количество учеников в каждом классе
class1 = int(input("Введите количество учеников в 1 классе: "))
class2 = int(input("Введите количество учеников во 2 классе: "))
class3 = int(input("Введите количество учеников в 3 классе: "))

# Так как за каждой партой могут сидеть максимум 2 ученика,
# для каждого класса делим количество учеников на 2 и округляем вверх
# math.ceil() округляет дробь вверх до ближайшего целого
import math

desks_class1 = math.ceil(class1 / 2)
desks_class2 = math.ceil(class2 / 2)
desks_class3 = math.ceil(class3 / 2)

# Суммируем количество парт для всех трех классов
total_desks = desks_class1 + desks_class2 + desks_class3

# Выводим результат
print("Всего нужно закупить парт:", total_desks)