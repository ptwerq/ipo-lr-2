# Импортируем модуль math, чтобы использовать число π и функцию возведения в степень
import math

# Ввод объема шара (X) от пользователя
X = float(input("Введите объем шара (X): "))

# Вычисляем радиус по формуле r = (3 * V / (4 * pi))^(1/3)
r = (3 * X / (4 * math.pi)) ** (1/3)

# Выводим радиус
print("Радиус шара равен:", r)